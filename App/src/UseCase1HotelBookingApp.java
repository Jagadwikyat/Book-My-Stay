/**
 * Use Case 1: Application Entry & Welcome Message
 *
 * <p>This class demonstrates the entry point of a Java application.
 * It prints a welcome message along with application details
 * to illustrate basic program execution flow.</p>
 *
 * <p><b>Concepts Covered:</b>
 * Class, main() method, static keyword, console output,
 * string literals, and linear execution flow.</p>
 *
 * @author Jagadwikyat
 * @version 1.0
 */
public class UseCase1HotelBookingApp {

    /**
     * Entry point of the application.
     *
     * @param args Command-line arguments
     */
    public static void main(String[] args) {

        // Welcome message
        System.out.println("=====================================");
        System.out.println("     Welcome to Book My Stay App     ");
        System.out.println("=====================================");

        // Application details
        System.out.println("Application: Hotel Booking System");
        System.out.println("Version: 1.0");

        // End message
        System.out.println("Application started successfully.");
    }
}